    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if(investigador != null){
            try{
                String msg = principal.enroll(this.investigador);
                JOptionPane.showMessageDialog(this, msg, "Informacion", JOptionPane.INFORMATION_MESSAGE);
            }catch(SQLException e){
                e.printStackTrace();
                JOptionPane.showMessageDialog(this, "No se puede registrar la huella en la BD.\nReporte el problema", "Error", JOptionPane.ERROR_MESSAGE);
            }

            this.dispose();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    public void mostrarDatos(Investigador investigador){
        setInvestigador(investigador);
        if(investigador == null || investigador.getNombre().compareTo("null") == 0){
            JOptionPane.showMessageDialog(this, "Error al cargar el Investigador.\nReporte el problema.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        labelNombre.setText( investigador.getApellido() + ", " + investigador.getNombre() );
        labelDni.setText(String.valueOf(investigador.getDni()));
        labelTelefono.setText(investigador.getTelefono());
        labelEmail.setText(investigador.getEmail());

    }

    public void setInvestigador(Investigador inv){
        this.investigador = inv;
    }

