    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        try{
            int i = Integer.parseInt(campoCodigo.getText());
            if (i <= 0)
                throw new NumberFormatException();
        }catch(NumberFormatException e){
            JOptionPane.showMessageDialog(this, "El dni debe estar compuesto solo por números, y ser distinto de cero.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        try{
            mostrarDatos(new Investigador(campoCodigo.getText()));
        }catch(NullPointerException npe){
            JOptionPane.showMessageDialog(this, "No existe ningún investigador con ese dni.", "No se encontró investigador", JOptionPane.INFORMATION_MESSAGE);
        }catch(SQLException sqle){
            JOptionPane.showMessageDialog(this, "Error de la base de datos.\nReporte el problema", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

